void main() {
  int a = 10;
  int b = 5;

  var penjumlahan = a + b;
  var pengurangan = a - b;
  var perkalian = a * b;
  var pembagian = a / b;
  var modulus = a % b;
  var increment = a++;
  var decrement = a--;

print("a + b = $penjumlahan");
print("a - b = $pengurangan");
print("a * b = $perkalian");
print("a / b = $pembagian");
print("a % b = $modulus");
print("a++ = $increment");
print("a-- = $decrement");
}